import Foundation

func solution(_ numbers:[Int]) -> String {
    var numbers = numbers
    numbers.sort(by: { (Int("\($0)\($1)") ?? 0) > (Int("\($1)\($0)") ?? 0)})
    if numbers[0] == 0 { return "0" }
    return numbers.map({String($0)}).joined()
}

